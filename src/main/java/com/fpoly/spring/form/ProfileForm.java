package com.fpoly.spring.form;

public class ProfileForm {
	String avatar;

	public ProfileForm() {
		super();
	}

	public ProfileForm(String avatar) {
		super();
		this.avatar = avatar;
	}

	public String getAvatar() {
		return avatar;
	}

	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}
	
	
}
